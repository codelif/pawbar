var rgbRE = regexp.MustCompile(
	`(?i)^\s*rgb\s*\(\s*` +
		`([0-9]{1,3})\s*,\s*` +
		`([0-9]{1,3})\s*,\s*` +
		`([0-9]{1,3})\s*\)\s*$`,
)

func ParseColor(s string) (vaxis.Color, error) {
	s = strings.TrimSpace(s)

	if strings.HasPrefix(s, "#") {
		h := strings.TrimPrefix(s, "#")

		switch len(h) {
		case 3:
			h = fmt.Sprintf("%c%c%c%c%c%c", h[0], h[0], h[1], h[1], h[2], h[2])
		case 6:
		default:
			return 0, fmt.Errorf("hex color %q must have 3 or 6 digits", s)
		}

		n, err := strconv.ParseUint(h, 16, 24)
		if err != nil {
			return 0, fmt.Errorf("bad hex color %q: %w", s, err)
		}
		return vaxis.HexColor(uint32(n)), nil
	}

	if m := rgbRE.FindStringSubmatch(s); m != nil {
		r, _ := strconv.Atoi(m[1])
		g, _ := strconv.Atoi(m[2])
		b, _ := strconv.Atoi(m[3])

		for _, v := range []struct {
			name string
			v    int
		}{
			{"R", r}, {"G", g}, {"B", b},
		} {
			if v.v > 255 {
				return 0, fmt.Errorf("%s component out of range in %q", v.name, s)
			}
		}
		return vaxis.RGBColor(uint8(r), uint8(g), uint8(b)), nil
	}

	if c, ok := named[strings.ToLower(s)]; ok {
		return c, nil
	}

	return 0, fmt.Errorf("unrecognised color %q", s)
}

